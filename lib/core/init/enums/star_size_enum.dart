import 'package:ice_cream_shop/core/widgets/star.dart';

/// Gives 3 different size options for star icon
/// ```dart
/// child: StarImage(isEmpty: false, size: StarSizes.MIN),
/// ```
/// [MIN] is equal to 15 pixels
/// [MEDIUM] is equal to 20 pixels
/// [MAX] is equal to 25 pixels
enum StarSizes { MIN, MEDIUM, MAX }
